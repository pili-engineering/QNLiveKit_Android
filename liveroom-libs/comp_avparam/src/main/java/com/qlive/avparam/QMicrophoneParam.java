package com.qlive.avparam;

/**
 * 麦克风参数
 */
public class QMicrophoneParam {
    /**
     * 采样率 默认值48000
     */
    public int sampleRate = 48000;
    /**
     * 采样位深 默认16
     */
    public int bitsPerSample = 16;
    /**
     * 通道数 默认 1
     */
    public int channelCount = 1;
    /**
     * 码率 默认64000
     */
    public int bitrate = 64000;
}
